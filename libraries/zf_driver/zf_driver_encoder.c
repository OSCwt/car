//  魹ｽ魹ｽ魹ｽ魹ｽﾋｵ魹ｽ魹ｽ      gptn            ﾑ｡魹ｽ魹ｽ魹ｽ魹ｽﾊｹ魹ｽﾃｵﾄｶ魹ｽﾊｱ魹ｽ魹ｽ
