//  魹ｽ魹ｽ魹ｽ魹ｽﾋｵ魹ｽ魹ｽ      ch1_pin         ENCODER ﾍｨ魹ｽ魹ｽ 1
//  魹ｽ魹ｽ魹ｽ魹ｽﾋｵ魹ｽ魹ｽ      ch2_pin         ENCODER ﾍｨ魹ｽ魹ｽ 2
